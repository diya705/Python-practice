#   steps for creating virtual environment 
# 1. write in terminal ' pip install virtualenv ' 
# 2. now give the name to that virtual environment ' virtualenv <env name>  '
# 3. to activate the virtual environment write this command ' .\<env name>\Scripts\activate.ps1 '
# 4. for deactivating the virtual environment write ' deactivate '
# 5. if you want to know the details of your all installed packages then run this command ' pip freeze'
# 6. if you want to get your all packages details in one txt file then run this command ' pip freeze > requirement.txt '
# 7. and if you want to install all the packages of requirement.txt in virtual environment then run this command ' pip install -r .\requirement.txt '